    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        data dt = new data(idInput.getText(), nameInput.getText(), alamatInput.getText(), telephoneInput.getText());
        list.add(dt);

        listData();

        idInput.setText("");
        nameInput.setText("");
        alamatInput.setText("");
        telephoneInput.setText("");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < list.size(); i++) {
            if (namaVar.equals(list.get(i).getNama())) {
                list.remove(i);
            }
        }
        listData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        idInput.setText(idVar);
        nameInput.setText(namaVar);
        alamatInput.setText(alamatVar);
        telephoneInput.setText(telephoneVar);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < list.size(); i++) {
            if (idVar.equals(list.get(i).getId())) {
                list.get(i).setId(idInput.getText());
                list.get(i).setNama(nameInput.getText());
                list.get(i).setAlamat(alamatInput.getText());
                list.get(i).setTelephone(telephoneInput.getText());
            }
        }
        idInput.setText("");
        nameInput.setText("");
        alamatInput.setText("");
        telephoneInput.setText("");
        
        listData();
    }//GEN-LAST:event_btnEditActionPerformed

