    public void listData() {
        String table[][] = new String[list.size()][4];
        for (int i = 0; i < list.size(); i++) {
            table[i][0] = list.get(i).getId();
            table[i][1] = list.get(i).getNama();
            table[i][2] = list.get(i).getAlamat();
            table[i][3] = list.get(i).getTelephone();
        }
        tblShow.setModel(new javax.swing.table.DefaultTableModel(table, new String[]{"id", "nama", "alamat", "telephone"}));
    }

